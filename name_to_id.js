// Helper to normalize Pokémon names for matching
function normalizeName(name) {
    if (!name) return "";
    let normalized = name.toLowerCase();

    // Specific character replacements first
    normalized = normalized
        .replace(/♀/g, 'f')        // Nidoran♀ -> nidoranf
        .replace(/♂/g, 'm')        // Nidoran♂ -> nidoranm
        .replace(/'/g, '')         // Farfetch'd -> farfetchd
        .replace(/\./g, '')        // Mr. Mime -> mrmime
        .replace(/: /g, '')        // Type: Null -> typenull
        .replace(/ \(\w+\)/g, '')  // Remove things like " (Glastrier)" from "As One (Glastrier)"
                                   // or " (Combat)" from "Tauros-Paldea (Combat)"
        .replace(/\[non-vanilla\]/gi, ''); // Remove [non-vanilla] tag

    // Handle "formname" by trying to remove it and see if base matches
    // This is tricky because order matters and some forms are part of the actual name in `mons`
    // Prioritize more specific form names in `mons` array
    
    // General space and hyphen cleanup
    normalized = normalized
        .replace(/\s+/g, '')      // Remove all spaces
        .replace(/-+/g, '');      // Remove ALL hyphens (e.g. "mega-x" -> "megax")
                                   // This helps match "charizardmegax" in text to "Charizard-Mega-X" in array

    return normalized.trim();
}


// Function to find species ID (1-indexed)
function findSpeciesId(nameToFind, monsArray) {
    const normalizedInputName = normalizeName(nameToFind);

    for (let i = 0; i < monsArray.length; i++) {
        if (normalizeName(monsArray[i]) === normalizedInputName) {
            return { id: i + 1, matchedName: monsArray[i], status: "Exact Match" };
        }
    }

    // Attempt common suffix stripping for forms if no exact match
    // This helps if `mons` has "Pikachu" and text has "Pikachugmax"
    const commonSuffixes = [
        "mega", "gmax", "alola", "galar", "hisui", "paldea", "origin", "therian",
        "blade", "shield", "totem", "ash", "school", "zen", "crowned", "neutral",
        "complete", "unbound", "busted", "pirouette", "resolute", "starter", "spikyeared",
        "hero", "midnight", "dusk", "sunny", "rainy", "snowy", "attack", "defense", "speed",
        "sandy", "trash", "sunshine", "heat", "wash", "frost", "fan", "mow", "sky",
        "bluestriped", "whitestriped", "summer", "autumn", "winter", "white", "black",
        "lowkey", "antique", "ice", "noice", "female", "hangry", "roaming", "artisan",
        "masterpiece", "wellspring", "hearthflame", "cornerstone", "bloodmoon",
        "terastal", "stellar", "combat", "blaze", "aqua", // for Paldean Tauros
        "normal" // For Castform-Normal, Terapagos-Normal etc.
    ];

    for (const suffix of commonSuffixes) {
        if (normalizedInputName.endsWith(suffix)) {
            const baseNameAttempt = normalizedInputName.slice(0, -suffix.length);
            for (let i = 0; i < monsArray.length; i++) {
                if (normalizeName(monsArray[i]) === baseNameAttempt) {
                    return { id: i + 1, matchedName: monsArray[i], status: `Form Match (stripped '${suffix}')` };
                }
            }
        }
    }
    
    // If still no match, try removing specific known prefixes/suffixes from the mon list names
    // e.g. if `mons` has "Castform-Normal" and text has "Castform"
    for (let i = 0; i < monsArray.length; i++) {
        let baseMonsArrayName = normalizeName(monsArray[i]);
        for (const suffix of commonSuffixes) {
            if (baseMonsArrayName.endsWith(suffix)) {
                baseMonsArrayName = baseMonsArrayName.slice(0, -suffix.length);
                break; // Only strip one suffix for this check
            }
        }
        if (baseMonsArrayName === normalizedInputName) {
             return { id: i + 1, matchedName: monsArray[i], status: `Base Match (text was base of form)` };
        }
    }


    return { id: null, matchedName: null, status: "No Match" };
}

